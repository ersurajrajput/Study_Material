// In the most general sense, a statement is a part of your program that can be executed. That is, a
// statement specifies an action. C categorizes statements into these groups:
// • Selection =  selection statements are if and switch. (The term conditional statement is often used in place of selection statement.)

// • Iteration =  The iteration statements are while, for, and do-while. These are also commonly called loop statements.

// • Jump =The jump statements are break, continue, goto, and return.


// • Label =The label statements include the case and default statements (discussed along with the switch  statement) and the label statement itself (discussed with goto)


// • Expression = Expression statements are statements composed of a valid expression. Block statements are simply blocks of code. (A block begins with a { and ends with a }.)

// • Block = Block statements are also referred to as compound statements
